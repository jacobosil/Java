
package javaapplication27;


public class JavaApplication27 {


    public static void main(String[] args) {
        //bucle While
        
        
        int i = 1;
        while(i <= 5) {
            System.out.println("numero " + i);
  //          i++; 
            i = i + 1; 
        } 
        System.out.println("fin programa");
        
        int j = 10;
        while(j >= 0) {
            System.out.println("numero" + j);
         //   j--;
         j = j - 1;
        }
        System.out.println("fin programa");
        
    }
    
}
